-- Data Output Protocol Format (DOPF) 10 Bytes Data Payload
--+----------+----------+---------+-----------+--------------+--------------+-------+-------+-------+-------+
--| BYTE9    | BYTE8    | BYTE7   | BYTE6     | BYTE5        | BYTE4        | BYTE3 | BYTE2 | BYTE1 | BYTE0 |
--+----------+----------+---------+-----------+--------------+--------------+-------+-------+-------+-------+
--| CAN_ID_H | CAN_ID_L | NODE_ID | DATA_TYPE | SERVICE_CODE | MESSAGE_CODE | DATA3 | DATA2 | DATA1 | DATA0 |
--+----------+----------+---------+-----------+--------------+--------------+-------+-------+-------+-------+

-- CAN_ID_H		High Byte CANBus ID (Follows CANAerospace std)
-- CAN_ID_L		Low Byte CANBus ID (Follows CANAerospace std)
-- NODE_ID		Default NODE_AHRS 0x01
-- DATA_TYPE	Type of payload data CHAR, FLOAT, UINT32 ...
-- SERVICE_CODE	Always SC4 Used on CANAerospace (Simulation Service Code)
-- MESSAGE_CODE Counter from 0 to 255
-- DATA3		Payload Data
-- DATA2		Payload Data
-- DATA1		Payload Data
-- DATA0		Payload Data

